public class MainJavaBeginnerLesson2 {

	public static void main(String[] args) {
		// 変数を扱う場合下記のようにコーディングする。

		// 変数に"Java Beginner ようこそ"を設定する
		String str = "Java Beginner ようこそ";
		// String 型
		// str 変数名
		// = 代入演算子
		// "Java Beginner ようこそ" 設定値

		// 変数に数字を設定する
		int num = 10;
		// int 型
		// num 変数名
		// = 代入演算子
		// 10 設定値

		// 文字を利用する場合、主にString型を利用する
		// 文字を利用する場合、主にString型を利用する

		// コンソールに変数に設定した"Java Beginner ようこそ"を表示する
		System.out.println(str);
		// コンソールに変数に設定した数字を表示する
		System.out.println(num);

		// ポイント
		// 代入演算子をイコールと読まないこと

	}
}
